package com.xiu.search.core.solr.params;

import com.xiu.solr.lexicon.client.common.MktTypeEnum;

/**
 * suggest的solr条件参数
 * @author Leon
 *
 */
public class SuggestFatParams {

	/**
	 * 搜索关键字
	 */
	private String keyword;
	/**
	 * 最大的返回数量
	 */
	private int maxRows;
	/**
	 * suggest类型
	 */
	private String type;
	
	/**
	 * 市场类型
	 */
	private MktTypeEnum mktType;
	
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public int getMaxRows() {
		return maxRows;
	}
	public void setMaxRows(int maxRows) {
		this.maxRows = maxRows;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public MktTypeEnum getMktType() {
		return mktType;
	}
	public void setMktType(MktTypeEnum mktType) {
		this.mktType = mktType;
	}
}
