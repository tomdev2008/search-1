package com.xiu.search.dao.model;

public class XTag implements Cloneable{

	private Integer id;
	
	private String name;
	
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	public XTag clone(){
		try {
			return (XTag) super.clone();
		} catch (CloneNotSupportedException e) {
		}
		return this;
	}
	
}
